class Fruit
{
    String name;
    int calo;
    int price;
    String color;


Fruit(String name,int calo,int price,String color)
{
    this.name=name;
    this.calo=calo;
    this.price=price;
    this.color=color;
}
public String getName() {
    return name;
}
public void setName(String name) {
    this.name = name;
}

public int getCalories() {
    return calo;
}
public void setCalories(int calories) {
    this.calo = calories;
}

public int getPrice() {
    return price;
}
public void setPrice(int price) {
    this.price = price;
}

public String getColor() {
    return color;
}
public void setColor(String color) {
    this.color = color;
}


}











